public class Topic {
	private int topicID;
	private int topicName;
	public int getTopicID() {
		return topicID;
	}
	public void setTopicID(int topicID) {
		this.topicID = topicID;
	}
	public int getTopicName() {
		return topicName;
	}
	public void setTopicName(int topicName) {
		this.topicName = topicName;
	}
	

}
